# Variable declaration and Assignment
name = "Chanaka Sampath"
num = 7
age = 26

# Multi-Assignment within a single statement
name, num, age = "Chanaka Sampath", 7, 26

# get the output
print(name)
print(num)
print(age)

# output multi values
print("Name : ", name, " his age : ", age, " and lucky number is ", num)
print("Name : " + name + " his age : " +
      str(age) + " and lucky number is ", str(num))
