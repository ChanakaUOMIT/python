# Arithmatic operators
print(100+70)  # 70
print(90-10)  # 80
print(10*4)  # 40
print(12/4)  # 3.0
print(12 % 5)  # 2
print(28 // 5)  # 5
print(2 ** 3)  # 8

# Comparison operators
print(3 == 3)
print(2 == 3)
print(10 > 11)
print(20 >= 15)
print(30 <= 18)

# Logical operations
print(5 == 5 and 10 == 10)
print(5 == 5 and 10 == 11)
print(5 == 5 or 10 == 11)
print(not True)

# Assignment operations
x = 5+10
print("x : ", x)

y = 8
y += 22
print("y : ", y)

z = 5
z /= 2
print("z : ", z)

# condition operations
name = "python"
